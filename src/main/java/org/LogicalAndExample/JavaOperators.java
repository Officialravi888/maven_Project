package org.LogicalAndExample;

public class JavaOperators {
    public static void main(String[] args) {
        int a = 58; //58 in binary is 111010
        int b = 13; //13 in binary is 1101
        System.out.println(a&b);  //returns 8 = 1000
        int x = 10;
        System.out.println(x<30 && x<60);  //returns false
    }
}
